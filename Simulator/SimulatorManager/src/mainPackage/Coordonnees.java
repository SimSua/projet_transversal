package mainPackage;

public class Coordonnees {
    private float x_coord;
    private float y_coord;

    public Coordonnees(float x,float y) {
        this.x_coord = x;
        this.y_coord = y;
    }

    public float getX_coord() {
        return this.x_coord;
    }

    public float getY_coord() {
        return this.y_coord;
    }
}
